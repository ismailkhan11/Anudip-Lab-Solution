# Using max() and min() functions display the maximum and minimum of 5 random numbers.

# Generate 5 random numbers.
random_numbers = [3,5,6,8,9]

# Use max() to find the maximum number
maximum_number = max(random_numbers)

# Use min() to find the minimum number
minimum_number = min(random_numbers)

# Display the maximum and minimum numbers
print("The maximum number is:", maximum_number)
print("The minimum number is:", minimum_number)


# Output:

# The maximum number is: 9
# The minimum number is: 3
