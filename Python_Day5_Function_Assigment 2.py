# Declare a square() function with one parameter.Then call the function and pass one number and display the square of that number

# Define a function named square with one parameter: num
def square(num):
    # Return the square of the input number (num * num)
    return num * num

# Call the square function with a number
result = square(5)

# Print the result of the square calculation
print("The square of the number is:", result)


# Output:

# The square of the number is: 25
