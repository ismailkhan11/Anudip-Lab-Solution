# Write a Python program to find the number of times 4 appears in the tuple. 
# Input: tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7 ) Output: 3 

# Define the tuple with the given values
tuplex = (2, 4, 5, 6, 2, 3, 4, 4, 7)

# Use the count() method to find how many times 4 appears in the tuple
count_of_4 = tuplex.count(4)

# Print the result
print("The number 4 appears", count_of_4, "times in the tuple.")


# Output:

# The number 4 appears 3 times in the tuple.
