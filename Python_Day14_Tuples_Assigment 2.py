# Write a Python program to convert a list to a tuple.
# Input: listx = [5, 10, 7, 4, 15, 3] Output: (5, 10, 7, 4, 15, 3)

# Define the list with the given values
listx = [5, 10, 7, 4, 15, 3]

# Convert the list to a tuple using the tuple() function
tuplex = tuple(listx)

# Print the result
print("The tuple is:", tuplex)


# Output:

# The tuple is: (5, 10, 7, 4, 15, 3)
