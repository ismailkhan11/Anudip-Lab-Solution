# Declare a div() function with two parameters. Then call the function and pass two numbers and display their division.

# Define a function named div with two parameters: num1 and num2
def div(num1, num2):
    # Return the result of dividing num1 by num2
    return num1 / num2

# Call the div function with two numbers
result = div(10, 2)

# Print the result of the division
print("The result of the division is:", result)


# Output:

# The result of the division is: 5.0
