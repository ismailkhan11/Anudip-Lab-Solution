# Accept a name from the user and display that in lower case using lower() function.

# Prompt the user to enter a name
name = input("Enter a name: ")

# Convert the entered name to lowercase using the lower() function
lowercase_name = name.lower()

# Display the name in lowercase
print("The name in lowercase is:", lowercase_name)


# Output:

# Enter a name: ISMAIL
# The name in lowercase is: ismail
