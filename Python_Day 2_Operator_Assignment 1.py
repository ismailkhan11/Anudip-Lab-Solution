# Calculate the multiplication and sum of two numbers

# Define two numbers
num1 = 5  
num2 = 10  

# Calculate the sum
sum = num1 + num2

# Calculate the multiplication
multiplication = num1 * num2

# Print the results
print("Sum of the two numbers is:", sum)
print("Multiplication of the two numbers is:", multiplication)


# Output:

# Sum of the two numbers is: 15

# Multiplication of the two numbers is: 50
